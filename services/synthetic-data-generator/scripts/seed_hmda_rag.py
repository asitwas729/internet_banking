def register_document(host: str, doc: dict, dry_run: bool) -> bool:
    url = f"{host}/api/internal/advisory/documents"
    headers = {"Content-Type": "application/json"}
    payload = {k: v for k, v in doc.items()}

    if dry_run:
        print(f"  [DRY-RUN] POST {url}")
        print(f"  docCd={doc['docCd']}, title={doc['docTitle'][:40]}...")
        return True

    try:
        resp = requests.post(url, headers=headers, data=json.dumps(payload), timeout=30)
        if resp.status_code in (200, 201):
            data = resp.json().get("data", {})
            print(f"  OK  docId={data.get('docId')} chunkCount={data.get('chunkCount')}")
            return True
        elif resp.status_code in (409, 422) or (
            resp.status_code == 400 and any(
                kw in resp.text for kw in ("이미", "중복", "LOAN_001", "duplicate")
            )
        ):
            print(f"  SKIP (이미 존재)  docCd={doc['docCd']} v{doc['docVersion']}")
            return True
        else:
            print(f"  FAIL  status={resp.status_code}  body={resp.text[:200]}")
            return False
    except requests.exceptions.ConnectionError as e:
        print(f"  FAIL  연결 오류: {e}")
        return False
    except Exception as e:
        print(f"  FAIL  {type(e).__name__}: {e}")
        return False


